def paragraph(text: str, style: str | None = None) -> str:
    ppr = f"<w:pPr><w:pStyle w:val=\"{style}\"/></w:pPr>" if style else ""
    if text == "":
        return f"<w:p>{ppr}</w:p>"
    lines = text.split("\n")
    runs = []
    for i, line in enumerate(lines):
        if i:
            runs.append("<w:r><w:br/></w:r>")
        runs.append(f"<w:r><w:t xml:space=\"preserve\">{escape(line)}</w:t></w:r>")
    return f"<w:p>{ppr}{''.join(runs)}</w:p>"


def parse_markdown(md_text: str) -> list[str]:
    paragraphs: list[str] = []
    for raw in md_text.splitlines():
        line = raw.rstrip()
        if not line.strip():
            paragraphs.append(paragraph(""))
            continue

        heading = re.match(r"^(#{1,3})\s+(.*)$", line)
        if heading:
            level = len(heading.group(1))
            style = "Title" if level == 1 and not paragraphs else f"Heading{min(level,3)}"
            paragraphs.append(paragraph(heading.group(2).strip(), style))
            continue

        if re.match(r"^\*\*.*\*\*$", line):
            paragraphs.append(paragraph(line.replace("**", ""), "Heading3"))
            continue

        if re.match(r"^\s*[-*]\s+", line):
            text = re.sub(r"^\s*[-*]\s+", "• ", line)
            paragraphs.append(paragraph(text))
            continue

        paragraphs.append(paragraph(line))
    return paragraphs


def build_document(paragraphs: list[str]) -> str:
    body = "".join(paragraphs)
    section = (
        "<w:sectPr>"
        "<w:pgSz w:w=\"11906\" w:h=\"16838\"/>"
        "<w:pgMar w:top=\"1440\" w:right=\"1440\" w:bottom=\"1440\" w:left=\"1440\" "
        "w:header=\"708\" w:footer=\"708\" w:gutter=\"0\"/>"
        "</w:sectPr>"
    )
    return (
        "<?xml version=\"1.0\" encoding=\"UTF-8\" standalone=\"yes\"?>"
        "<w:document xmlns:wpc=\"http://schemas.microsoft.com/office/word/2010/wordprocessingCanvas\" "
        "xmlns:mc=\"http://schemas.openxmlformats.org/markup-compatibility/2006\" "
        "xmlns:o=\"urn:schemas-microsoft-com:office:office\" "
        "xmlns:r=\"http://schemas.openxmlformats.org/officeDocument/2006/relationships\" "
        "xmlns:m=\"http://schemas.openxmlformats.org/officeDocument/2006/math\" "
        "xmlns:v=\"urn:schemas-microsoft-com:vml\" "
        "xmlns:wp14=\"http://schemas.microsoft.com/office/word/2010/wordprocessingDrawing\" "
        "xmlns:wp=\"http://schemas.openxmlformats.org/drawingml/2006/wordprocessingDrawing\" "
        "xmlns:w10=\"urn:schemas-microsoft-com:office:word\" "
        "xmlns:w=\"http://schemas.openxmlformats.org/wordprocessingml/2006/main\" "
        "xmlns:w14=\"http://schemas.microsoft.com/office/word/2010/wordml\" "
        "xmlns:wpg=\"http://schemas.microsoft.com/office/word/2010/wordprocessingGroup\" "
        "xmlns:wpi=\"http://schemas.microsoft.com/office/word/2010/wordprocessingInk\" "
        "xmlns:wne=\"http://schemas.microsoft.com/office/2006/wordml\" "
        "xmlns:wps=\"http://schemas.microsoft.com/office/word/2010/wordprocessingShape\" "
        "mc:Ignorable=\"w14 wp14\">"
        f"<w:body>{body}{section}</w:body></w:document>"
    )


def main() -> int:
    if len(sys.argv) != 3:
        print("Usage: python tools/markdown_to_docx.py input.md output.docx")
        return 1

    input_path = Path(sys.argv[1])
    output_path = Path(sys.argv[2])
    title = input_path.stem
    md_text = input_path.read_text(encoding="utf-8")
    doc_xml = build_document(parse_markdown(md_text))

    output_path.parent.mkdir(parents=True, exist_ok=True)
    with zipfile.ZipFile(output_path, "w", compression=zipfile.ZIP_DEFLATED) as zf:
        zf.writestr("[Content_Types].xml", CONTENT_TYPES)
        zf.writestr("_rels/.rels", ROOT_RELS)
        zf.writestr("word/document.xml", doc_xml)
        zf.writestr("word/styles.xml", STYLES)
        zf.writestr("word/_rels/document.xml.rels", DOCUMENT_RELS)
        zf.writestr("docProps/app.xml", APP_XML)
        zf.writestr("docProps/core.xml", CORE_XML.format(title=escape(title)))

    print(output_path)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
